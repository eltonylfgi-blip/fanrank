drop function if exists public.fr_team_submission_inbox(text, integer);
create function public.fr_team_submission_inbox(
  p_section text,
  p_limit integer default 50
)
returns table (
  id bigint,
  section text,
  title text,
  details text,
  author text,
  contact text,
  language text,
  status text,
  canonical_idea_id integer,
  category_requested text,
  category_final text,
  classification_method text,
  attachment_count smallint,
  evidence_links text[],
  can_contact boolean,
  topic_id bigint,
  topic_title text,
  created_at timestamptz
)
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_actor uuid := auth.uid();
  v_section text := pg_catalog.btrim(p_section);
  v_limit integer := least(greatest(coalesce(p_limit, 50), 1), 50);
  v_authorized boolean;
begin
  if v_actor is null then
    raise exception using errcode = '42501', message = 'Authenticated team member required';
  end if;
  if v_section is null or pg_catalog.char_length(v_section) not between 1 and 120 then
    raise exception using errcode = '22023', message = 'Valid profile section required';
  end if;

  select fanrank_private.fr_can_manage_profile(v_section, v_actor) into v_authorized;
  if not coalesce(v_authorized, false) then
    raise exception using errcode = '42501', message = 'Verified profile owner or administrator required';
  end if;

  return query
  select
    submission.id,
    submission.section,
    submission.title,
    submission.details,
    case when submission.allow_contact then submission.author else null end,
    case when submission.allow_contact then submission.contact else null end,
    submission.language,
    submission.status,
    submission.canonical_idea_id,
    submission.category_requested,
    submission.category_final,
    submission.classification_method,
    submission.attachment_count,
    submission.evidence_links,
    submission.allow_contact,
    submission.topic_id,
    topic.title,
    submission.created_at
  from public.fr_submissions as submission
  left join public.fr_profile_topics as topic on topic.id = submission.topic_id
  where submission.section = v_section
    and submission.status = 'received'
  order by submission.created_at desc, submission.id desc
  limit v_limit;
end;
$$;

