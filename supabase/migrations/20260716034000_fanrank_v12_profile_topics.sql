create or replace function fanrank_private.fr_topic_limit(p_tier text)
returns integer
language sql
immutable
set search_path = ''
as $$
  select case p_tier
    when 'normal' then 5
    when 'pro' then 20
    when 'business' then 100
    when 'plus' then 200
    else 5
  end;
$$;

create or replace function fanrank_private.fr_can_manage_profile(
  p_section text,
  p_actor uuid
)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select p_actor is not null and (
    exists (
      select 1
      from public.fr_platform_admins as admin_row
      where admin_row.user_id = p_actor
        and admin_row.status = 'active'
    )
    or exists (
      select 1
      from public.fr_profile_members as member_row
      join public.fr_sections as section_row on section_row.slug = member_row.section
      where member_row.user_id = p_actor
        and member_row.section = p_section
        and member_row.role in ('owner', 'admin')
        and member_row.status = 'active'
        and section_row.verification_status = 'verified'
    )
  );
$$;

revoke all on function fanrank_private.fr_topic_limit(text) from public, anon, authenticated;
revoke all on function fanrank_private.fr_can_manage_profile(text, uuid) from public, anon, authenticated;

create or replace function fanrank_private.fr_enforce_topic_limit()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_tier text;
  v_active_count integer;
begin
  if new.status <> 'active'
    or (tg_op = 'UPDATE' and old.status = 'active' and old.section = new.section)
  then
    return new;
  end if;

  select section_row.topic_tier
    into v_tier
  from public.fr_sections as section_row
  where section_row.slug = new.section
  for update;
  if v_tier is null then
    raise exception using errcode = '23503', message = 'Profile does not exist';
  end if;

  select count(*)::integer
    into v_active_count
  from public.fr_profile_topics as topic
  where topic.section = new.section
    and topic.status = 'active'
    and (tg_op = 'INSERT' or topic.id <> new.id);

  if v_active_count >= fanrank_private.fr_topic_limit(v_tier) then
    raise exception using errcode = '23514', message = 'Active topic limit reached';
  end if;
  return new;
end;
$$;

revoke all on function fanrank_private.fr_enforce_topic_limit()
  from public, anon, authenticated;
drop trigger if exists fr_profile_topics_enforce_limit on public.fr_profile_topics;
create trigger fr_profile_topics_enforce_limit
  before insert or update of section, status on public.fr_profile_topics
  for each row execute function fanrank_private.fr_enforce_topic_limit();

create or replace function fanrank_private.fr_validate_topic_assignment()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_topic_section text;
  v_topic_status text;
begin
  if new.topic_id is null then
    return new;
  end if;

  select topic.section, topic.status
    into v_topic_section, v_topic_status
  from public.fr_profile_topics as topic
  where topic.id = new.topic_id;

  if v_topic_section is null then
    raise exception using errcode = '23503', message = 'Topic does not exist';
  end if;
  if v_topic_section <> new.section then
    raise exception using errcode = '23514', message = 'Topic belongs to another profile';
  end if;
  if v_topic_status <> 'active' then
    raise exception using errcode = '23514', message = 'Topic is archived';
  end if;
  return new;
end;
$$;

revoke all on function fanrank_private.fr_validate_topic_assignment()
  from public, anon, authenticated;

drop trigger if exists fr_submissions_validate_topic on public.fr_submissions;
create trigger fr_submissions_validate_topic
  before insert or update of section, topic_id on public.fr_submissions
  for each row execute function fanrank_private.fr_validate_topic_assignment();

drop trigger if exists fr_ideas_validate_topic on public.fr_ideas;
create trigger fr_ideas_validate_topic
  before insert or update of section, topic_id on public.fr_ideas
  for each row execute function fanrank_private.fr_validate_topic_assignment();

create or replace function public.fr_upsert_profile_topic(
  p_section text,
  p_topic_id bigint,
  p_title text,
  p_description text,
  p_sort_order integer
)
returns table (
  id bigint,
  section text,
  title text,
  description text,
  status text,
  sort_order integer,
  active_count integer,
  topic_limit integer
)
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_actor uuid := auth.uid();
  v_section text := pg_catalog.btrim(p_section);
  v_title text := pg_catalog.btrim(p_title);
  v_description text := nullif(pg_catalog.btrim(p_description), '');
  v_tier text;
  v_limit integer;
  v_active_count integer;
  v_existing_status text;
  v_topic public.fr_profile_topics%rowtype;
begin
  if v_actor is null then
    raise exception using errcode = '42501', message = 'Authenticated profile manager required';
  end if;
  if v_section is null or pg_catalog.char_length(v_section) not between 1 and 120
    or v_title is null or pg_catalog.char_length(v_title) not between 2 and 80
    or (v_description is not null and pg_catalog.char_length(v_description) > 500)
    or coalesce(p_sort_order, 100) not between 0 and 10000
  then
    raise exception using errcode = '22023', message = 'Valid topic fields required';
  end if;

  select section_row.topic_tier
    into v_tier
  from public.fr_sections as section_row
  where section_row.slug = v_section
  for update;

  if v_tier is null then
    raise exception using errcode = '22023', message = 'Profile does not exist';
  end if;
  if not fanrank_private.fr_can_manage_profile(v_section, v_actor) then
    raise exception using errcode = '42501', message = 'Verified profile owner or administrator required';
  end if;

  v_limit := fanrank_private.fr_topic_limit(v_tier);
  select count(*)::integer
    into v_active_count
  from public.fr_profile_topics as topic
  where topic.section = v_section
    and topic.status = 'active';

  if p_topic_id is null then
    if v_active_count >= v_limit then
      raise exception using errcode = '23514', message = 'Active topic limit reached';
    end if;
    insert into public.fr_profile_topics (
      section, title, description, status, sort_order, created_by, updated_by
    ) values (
      v_section, v_title, v_description, 'active', coalesce(p_sort_order, 100), v_actor, v_actor
    ) returning * into v_topic;
    v_active_count := v_active_count + 1;
  else
    select topic.status
      into v_existing_status
    from public.fr_profile_topics as topic
    where topic.id = p_topic_id
      and topic.section = v_section
    for update;
    if v_existing_status is null then
      raise exception using errcode = '22023', message = 'Topic does not belong to this profile';
    end if;
    if v_existing_status <> 'active' then
      raise exception using errcode = '22023', message = 'Archived topics cannot be edited';
    end if;
    update public.fr_profile_topics as topic
      set title = v_title,
          description = v_description,
          sort_order = coalesce(p_sort_order, 100),
          updated_by = v_actor,
          updated_at = pg_catalog.now()
    where topic.id = p_topic_id
      and topic.section = v_section
    returning topic.* into v_topic;
  end if;

  return query select
    v_topic.id, v_topic.section, v_topic.title, v_topic.description,
    v_topic.status, v_topic.sort_order, v_active_count, v_limit;
end;
$$;

create or replace function public.fr_archive_profile_topic(
  p_section text,
  p_topic_id bigint
)
returns table (
  id bigint,
  status text,
  active_count integer,
  topic_limit integer
)
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_actor uuid := auth.uid();
  v_section text := pg_catalog.btrim(p_section);
  v_tier text;
  v_active_count integer;
begin
  if v_actor is null then
    raise exception using errcode = '42501', message = 'Authenticated profile manager required';
  end if;
  select section_row.topic_tier
    into v_tier
  from public.fr_sections as section_row
  where section_row.slug = v_section
  for update;
  if v_tier is null then
    raise exception using errcode = '22023', message = 'Profile does not exist';
  end if;
  if not fanrank_private.fr_can_manage_profile(v_section, v_actor) then
    raise exception using errcode = '42501', message = 'Verified profile owner or administrator required';
  end if;

  update public.fr_profile_topics as topic
    set status = 'archived', updated_by = v_actor, updated_at = pg_catalog.now()
  where topic.id = p_topic_id
    and topic.section = v_section
    and topic.status = 'active';
  if not found then
    raise exception using errcode = '22023', message = 'Active topic does not belong to this profile';
  end if;

  select count(*)::integer
    into v_active_count
  from public.fr_profile_topics as topic
  where topic.section = v_section
    and topic.status = 'active';

  return query select p_topic_id, 'archived'::text, v_active_count,
    fanrank_private.fr_topic_limit(v_tier);
end;
$$;

create or replace function public.fr_set_idea_topic(
  p_idea_id integer,
  p_topic_id bigint
)
returns table (
  idea_id integer,
  section text,
  topic_id bigint,
  topic_title text
)
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_actor uuid := auth.uid();
  v_section text;
  v_topic_title text;
begin
  if v_actor is null then
    raise exception using errcode = '42501', message = 'Authenticated profile manager required';
  end if;
  select idea.section
    into v_section
  from public.fr_ideas as idea
  where idea.id = p_idea_id
  for update;
  if v_section is null then
    raise exception using errcode = '22023', message = 'Idea does not exist';
  end if;
  perform 1 from public.fr_sections as section_row where section_row.slug = v_section for update;
  if not fanrank_private.fr_can_manage_profile(v_section, v_actor) then
    raise exception using errcode = '42501', message = 'Verified profile owner or administrator required';
  end if;

  if p_topic_id is not null then
    select topic.title
      into v_topic_title
    from public.fr_profile_topics as topic
    where topic.id = p_topic_id
      and topic.section = v_section
      and topic.status = 'active';
    if v_topic_title is null then
      raise exception using errcode = '22023', message = 'Active topic does not belong to this profile';
    end if;
  end if;

  update public.fr_ideas as idea
    set topic_id = p_topic_id
  where idea.id = p_idea_id;

  return query select p_idea_id, v_section, p_topic_id, v_topic_title;
end;
$$;

