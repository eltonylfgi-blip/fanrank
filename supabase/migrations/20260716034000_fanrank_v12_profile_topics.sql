create table public.fr_profile_topics (
  id bigint generated by default as identity primary key,
  section text not null references public.fr_sections(slug) on update restrict on delete cascade,
  title text not null check (pg_catalog.char_length(pg_catalog.btrim(title)) between 2 and 80),
  description text check (description is null or pg_catalog.char_length(description) <= 500),
  status text not null default 'active' check (status in ('active', 'archived')),
  sort_order integer not null default 100 check (sort_order between 0 and 10000),
  created_by uuid references auth.users(id) on delete set null,
  updated_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default pg_catalog.now(),
  updated_at timestamptz not null default pg_catalog.now()
);

create unique index fr_profile_topics_active_title_unique
  on public.fr_profile_topics(section, pg_catalog.lower(pg_catalog.btrim(title)))
  where status = 'active';
create index fr_profile_topics_section_status_order_idx
  on public.fr_profile_topics(section, status, sort_order, id);

